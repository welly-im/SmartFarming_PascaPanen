package com.example.smartfarming_pascapanen.RawData;

public class ListRawData {
    String id_panen, berat, tanggal_panen, id_pengguna, nama_pengguna;

    public ListRawData() {
    }

    public String getId_panen() {
        return id_panen;
    }

    public void setId_panen(String id_panen) {
        this.id_panen = id_panen;
    }

    public String getBerat() {
        return berat;
    }

    public void setBerat(String berat) {
        this.berat = berat;
    }

    public String getTanggal_panen() {
        return tanggal_panen;
    }

    public void setTanggal_panen(String tanggal_panen) {
        this.tanggal_panen = tanggal_panen;
    }

    public String getId_pengguna() {
        return id_pengguna;
    }

    public void setId_pengguna(String id_pengguna) {
        this.id_pengguna = id_pengguna;
    }

    public String getNama_pengguna() {
        return nama_pengguna;
    }

    public void setNama_pengguna(String nama_pengguna) {
        this.nama_pengguna = nama_pengguna;
    }
}
