package com.example.smartfarming_pascapanen.GudangStok;

public class DataModelKopiSiapDistribusi {
    String id_stok, berat_kopi, harga_jual, grade;

    public String getId_stok() {
        return id_stok;
    }

    public void setId_stok(String id_stok) {
        this.id_stok = id_stok;
    }

    public String getBerat_kopi() {
        return berat_kopi;
    }

    public void setBerat_kopi(String berat_kopi) {
        this.berat_kopi = berat_kopi;
    }

    public String getHarga_jual() {
        return harga_jual;
    }

    public void setHarga_jual(String harga_jual) {
        this.harga_jual = harga_jual;
    }

    public String getGrade() {
        return grade;
    }

    public void setGrade(String grade) {
        this.grade = grade;
    }
}
