package com.example.smartfarming_pascapanen.GudangStok.JualKopi;

public class DataModelGudangStokPenjualanKopi {
    String id_stok, grade, berat, harga_jual,harga_jual_per_kg, keterangan;

    public String getId_stok() {
        return id_stok;
    }

    public void setId_stok(String id_stok) {
        this.id_stok = id_stok;
    }

    public String getGrade() {
        return grade;
    }

    public void setGrade(String grade) {
        this.grade = grade;
    }

    public String getBerat() {
        return berat;
    }

    public void setBerat(String berat) {
        this.berat = berat;
    }

    public String getHarga_jual() {
        return harga_jual;
    }

    public void setHarga_jual(String harga_jual) {
        this.harga_jual = harga_jual;
    }

    public String getHarga_jual_per_kg() {
        return harga_jual_per_kg;
    }

    public void setHarga_jual_per_kg(String harga_jual_per_kg) {
        this.harga_jual_per_kg = harga_jual_per_kg;
    }

    public String getKeterangan() {
        return keterangan;
    }

    public void setKeterangan(String keterangan) {
        this.keterangan = keterangan;
    }
}
